set1 = {2,4,6,8,10}
set2 = {2,3,5,7,9,11}
def union():
    print("Purpose of union is: Union of two given sets is the smallest set which contains all the elements of both the sets")
    print(set1.union(set2))

def intersect():
    print("Intersection is:")
    print(set1.intersection(set2))

union()
intersect()